    	<h1>What is A Room Too Far?</h1>
    	<p><b>A Room Too Far</b> is a cooperative dungeon crawler with a persistent user-generated world on an arcade machine. Dungeons are created on a companion website and are then downloaded onto an arcade machine where players can interact with an ever expanding world unique to the each machine. Players explore a <b>cyberpunk</b> world overwhelmed by the alarming growth of a <b>jungle</b>.</p>
        <p>We are inspired by games such as Gauntlet, Zelda, Dungeon Keeper, and Diablo.</p>
        <p>A Room Too Far is being developed through the Computer Game Design program at University of California, Santa Cruz. </p>
		<h1>The story so far...</h1>
    	<p>There was once a civilization at the dawn of a new age. Their potential for progress was bright. One day everything changed. A jungle began to erupt, spreading across the planet, engulfing and consuming cities at a fantastic rate. They scrambled to build upwards, out of the reach of the rampant growth, into the skies.</p>
		<p>The safety of the clouds was not to last. Day by day the vicious vegetation chased them upwards until they could no longer build any further. Out of desperation they raised their cities and the surrounding lands into the sky. The very surface of the planet was torn asunder.</p>
		<p>Again they were safe, but only for a time...</p>
		<p>The jungle grew upward, surpassing their wildest expectation. One by one the brambles and branches began to pull their grand cities back down to the earth. In a final frenzy of desperation they did the only thing they could do, burn it all to the ground. The shining metal husks of their towers were only thing left standing amongst the ashes. Their precious ground freed.</p>
		<p>The cycle continues to this day. A cycle of ascension, reconstruction, survival, descent, and immulation. Adventurers delve into the ruins of their forsaken cities to gain power and resources for themselves and their civilization. The world keeps turning, forever changed, as they search for their salvation. A salvation that is always a room too far.</p>
